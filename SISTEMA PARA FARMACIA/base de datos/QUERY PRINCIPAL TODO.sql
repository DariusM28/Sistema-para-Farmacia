
--creamso la base de datos--
CREATE DATABASE FARMACIA_CURITA;
USE FARMACIA_CURITA;
--cremaos tabla de usuarios
CREATE TABLE USUARIOS(
ID_PERSONA INT IDENTITY (1,1) PRIMARY KEY,--hace autoincrementable el campo
PUESTO VARCHAR(14) NOT NULL,
NOMBRE VARCHAR(28) NOT NULL,
APELLIDO VARCHAR(28) NOT NULL,
EDAD INT NOT NULL,
SEXO VARCHAR(1) NOT NULL,--"m"/"f"--
ROL1 BIT NOT NULL DEFAULT 0,--rol de administrador
ROL2 BIT NOT NULL DEFAULT 0,--rol de administrador
ROL3 BIT NOT NULL DEFAULT 0,--rol de administrador
ROL4 BIT NOT NULL DEFAULT 0,--rol de cajas
ROL5 BIT NOT NULL DEFAULT 0,--rol de cajas
US VARCHAR(28) NOT NULL,--usuario
CONT VARCHAR(15) NOT NULL,--contraseña
ACTIVO BIT NOT NULL DEFAULT 0,--pernitira validar si el usuario puede trabjar
FECHA_CREACION_USUARIOS DATETIME NOT NULL DEFAULT GETDATE()--inserta la fecha del servidor por defecto 
);
